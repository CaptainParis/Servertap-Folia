package io.servertap.api.platform;

/**
 * Enum representing the different platform types supported by ServerTap
 */
public enum PlatformType {
    /**
     * Bukkit/Spigot/Paper/Folia platform
     */
    BUKKIT,
    
    /**
     * BungeeCord platform
     */
    BUNGEECORD,
    
    /**
     * Velocity platform
     */
    VELOCITY,
    
    /**
     * Unknown platform
     */
    UNKNOWN
}
